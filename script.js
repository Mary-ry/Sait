function sendMessage() {
    const text = userInput.value.trim().toLowerCase();
    
    if (text === "") return;

    addMessage(userInput.value, 'user-message');
    userInput.value = '';

    setTimeout(() => {
        const botResponse = getBotResponse(text);
        addMessage(botResponse, 'bot-message');
    }, 500);
}

